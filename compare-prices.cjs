async function main() {
  // Get all SKUs from DB
  const { data: dbSkus } = await sb.from('product_skus').select('id, sku_code, cost_price, retail_price, product_id, products:product_id(id, name, brand)').order('sku_code');
  const { data: dbProducts } = await sb.from('products').select('id, name, cost_price, retail_price, brand').order('created_at');
  
  const dbByCode = {};
  dbSkus.forEach(s => { dbByCode[s.sku_code] = s; });
  const dbByName = {};
  dbProducts.forEach(p => { dbByName[p.name] = p; });

  let costDiff = 0, retailDiff = 0, missing = 0, brandDiff = 0;
  const updates = [];

  for (const [code, name, cat, brand, cost, retail] of feishuData) {
    const sku = dbByCode[code];
    if (!sku) { missing++; console.log('❌ DB缺失:', code, name); continue; }
    
    let needsUpdate = false;
    const changes = [];
    
    // Check cost price
    if (cost !== null && sku.cost_price !== cost) {
      changes.push(`成本: ${sku.cost_price} → ${cost}`);
      needsUpdate = true;
    }
    // Check retail price
    if (retail !== null && sku.retail_price !== retail) {
      changes.push(`零售: ${sku.retail_price} → ${retail}`);
      needsUpdate = true;
    }
    // Check brand on product
    if (brand && sku.products?.brand !== brand) {
      changes.push(`品牌: ${sku.products?.brand || '空'} → ${brand}`);
      needsUpdate = true;
      brandDiff++;
    }
    
    if (needsUpdate) {
      retailDiff++;
      updates.push({ sku_id: sku.id, product_id: sku.product_id, code, name, changes });
    }
    costDiff++;
  }

  console.log(`\n=== 对比结果 ===`);
  console.log(`飞书表格单品: ${feishuData.length}`);
  console.log(`DB已有匹配: ${costDiff}`);
  console.log(`DB缺失产品: ${missing}`);
  console.log(`价格有差异: ${retailDiff}`);
  console.log(`品牌有差异: ${brandDiff}`);
  
  if (updates.length > 0) {
    console.log(`\n=== 需要更新的 ${updates.length} 条 ===`);
    for (const u of updates) {
      console.log(`${u.code} ${u.name}: ${u.changes.join(', ')}`);
    }
  }
}

main().catch(e => console.error(e.message));
